public class Passenger{
  String name;
  String seatType;
  int luggage;
  
  
  
  
  public Passenger(String name2, String seatType2, int luggage2){
    name = name2;
    seatType = seatType2;
    luggage = luggage2;
  }
  
  
  public String getName(){
  return name;
  }
  
  
  public String getSeatType(){
    return seatType;
  }
  
  public int getLuggage(){
    return luggage;
  }
  
  
  
}
    
    
   